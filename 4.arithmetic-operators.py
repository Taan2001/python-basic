# Addition: +
print("Addition: +")
print(3 + 7)

# Subtraction: -
print("Subtraction: -")
print(10 - 20)

# Multiplication: *
print("Multiplication: *")
print(3 * 10)

# Division: / return a float
print("Division: /") 
print(10 / 3)
print(type(10 / 2))
print(int(10 / 3)) # return a int

# Module: % return a int
print("Module: %") 
print(10 % 3)
print(type(10 % 2))
print(int(-10 % 3)) # return a int

# Exponentiation: ** 
print("Exponentiation: **") 
print(2**2)
print(2.5**2)
print(2**2.5)
print(type(2**2))
print(type(2.5**2))
print(type(2**2.5))

# Floor Division: // N = D * (N // D) + (N % D) return a int
print("Floor Division: //") 
print(10 // 3)
print(-10 // 3)
print(type(10 // 2))
print(type(-10 // 3))